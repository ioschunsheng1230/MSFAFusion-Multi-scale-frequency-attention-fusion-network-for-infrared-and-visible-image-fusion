def rfft(x, d):
    t = rfft2(x, dim=(-d, -1))
    return torch.stack((t.real, t.imag), -1)


def irfft(x, d, signal_sizes):
    return irfft2(torch.complex(x[:, :, 0], x[:, :, 1]), s=signal_sizes, dim=(-d, -1))


